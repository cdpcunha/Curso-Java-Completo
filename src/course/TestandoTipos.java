package course;

public class TestandoTipos {

	public static void main(String[] args) {
	byte x = 10; //variável X tipo Byte
	System.out.println(x);
	x= 120;
	System.out.println(x);
	char gender = 'F';
	System.out.println(gender);
	long n1 = 99999999L; 	//Longe tem que ter L no final da variável
	System.out.println(n1);
	float n2 = 4.5f ;//floats devem ter a letra f minúscula na variável
	System.out.println(n2);
	double n3 = 4.5;
	System.out.println(n3);
	}

}
